function check_strategy(id){
	var show = false;
	$.ajax({
		url : base.url+'strategy/get_strategy',
		type : 'POST',
		cache : false,
		async : false,
		data : {id:id},
		success : function (result){
			result = $.parseJSON(result);
			if(result.status){
				$('.strategy_name').html(result.msg.title);
				$('.strategy_position').html(result.msg.position);
				$('.strategy_profit').html(result.msg.profit);
				$('.strategy_stop').html(result.msg.stop);
				$('.strategy_reason').html(result.msg.reason);
				show = true;
			}else{
				alert('对不起，您现在无权查看该策略，请联系客服开通');
				show = false;
			}
		}
	})
	return show;
}

