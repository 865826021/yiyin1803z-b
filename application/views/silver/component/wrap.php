function focus_password(obj){
	var dom = $(obj);
	if(dom.val() == '请输入密码'){
		var type_password = $('<input type="password" name="pwd" onfocus="focus_password(this);" onblur="blur_password(this)">');
		$(dom.parent()).html(type_password);
		type_password.focus();
	}
}

