function send_sns(obj){
	var dom = $(obj);
	dom.attr("disabled", true);
	var phone = $('.dialog-content input[name=phone]').val();
	$.post(base.url+'sns/send_captcha',
	{phone:phone},
	function (d){
		d = $.parseJSON(d);
		if(d.status){
			var times = 60;
			var timer = setInterval(function () {
				if (times > 0) {
					dom.css("opacity", "0.3");
					dom.css("fontSize", "12");
					// dom.html(times + "秒重新获取");
					times--;
				} else {
					dom.removeAttr("disabled");
					dom.css("opacity", "1");
					// dom.html("获取验证码");
					clearInterval(timer);
					dom.removeAttr("disabled");
				}
			}, 1000);
			alert('验证码已发送，请用手机查收');
		}else{
			alert(d.msg);
			dom.removeAttr("disabled");
		}
	})
}


