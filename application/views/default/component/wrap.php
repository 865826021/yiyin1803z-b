		d = $.parseJSON(d);
		if(d.status){
			var times = 60;
			var timer = setInterval(function () {
				if (times > 0) {
					dom.css("opacity", "0.3");
					dom.css("fontSize", "12");
					dom.html(times + "秒重新获取");
					times--;
				} else {
					dom.removeAttr("disabled");
					dom.css("opacity", "1");
					dom.html("获取验证码");
					clearInterval(timer);
					dom.removeAttr("disabled");
				}
			}, 1000);
			alert('验证码已发送，请用手机查收');
		}else{
			alert(d.msg);
			dom.removeAttr("disabled");
		}
	})
}


function register(){
	var pwd = $('.dialog-content input[name=pwd]').val();
	var repwd = $('.dialog-content input[name=repwd]').val();
	if(pwd != repwd){
		alert('两次密码输入不一致');
		return;
	}
	$.post(base.url+'user/register',
	$('.dialog-content .register_form').serialize(),
	function(result){
		result = $.parseJSON(result);
		if(result.status){
			alert('恭喜您，注册成功！请登陆');
			$('.show_login').click();
		}else{
			alert(result.msg);
		}
	})
}

